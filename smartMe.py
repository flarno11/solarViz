def import_data(username, password, device_id, log_collection):
    csv = fetch_file(username, password, device_id)
    #csv = fetch_dummy()

    def create_element(items):
        return {'whAbs': int(items[2]), 'time': datetime.datetime.strptime(items[0], '%m/%d/%Y %I:%M:%S %p')}

    data = [create_element(line.split(';')) for line in csv.split('\n')[1:] if line]  # [1:] skip the first header element

    existing_entry = log_collection.find_one({'deviceId': device_id}, sort=[('time', pymongo.DESCENDING), ])
    if existing_entry:
        start_time = existing_entry['time']
        wh_abs = existing_entry['wattHoursAbsolute']
    else:
        start_time = datetime.datetime(2010, month=1, day=1, hour=0)
        wh_abs = 0

    first = True
    current_time = start_time

    for d in data:
        if d['time'].replace(minute=0, second=0, microsecond=0)\
                > current_time.replace(minute=0, second=0, microsecond=0):
            wh_diff = d['whAbs'] - wh_abs
            wh_abs = d['whAbs']
            if not first:
                log_collection.insert({'deviceId': device_id,
                                       'time': current_time,
                                       'wattHours': wh_diff,
                                       'wattHoursAbsolute': wh_abs,
                                       'updatedAt': datetime.datetime.utcnow()
                                       })

            first = False
            current_time = d['time'].replace(minute=0, second=0, microsecond=0)

    return [start_time, current_time, ]


def fetch_file(username, password, device_id):
    s = requests.Session()
    # s.get('http://httpbin.org/cookies/set/sessioncookie/123456789')
    # r = s.get('http://httpbin.org/cookies')
    # print(r.text)
    # '{"cookies": {"sessioncookie": "123456789"}}'

    res = s.get('https://smart-me.com/Details/All.aspx')
    soup_mysite = BeautifulSoup(res.text, "html.parser")
    input_fields = {input.get('name'): input.get('value') for input in soup_mysite.find_all("input")}
    input_fields['ctl00$ContentPlaceHolder1$Login1$UserName'] = username
    input_fields['ctl00$ContentPlaceHolder1$Login1$Password'] = password

    res = s.post('https://smart-me.com/SignIn.aspx?ReturnUrl=%2fDetails%2fAll.aspx', data=input_fields, allow_redirects=False)

    print(res)
    if 'Location' in res.headers:
        print(res.headers['Location'])
        if res.headers['Location'] != '/Details/All.aspx':
            raise LoginException()
    else:
        raise LoginException()

    res = s.get('https://smart-me.com/Details/Export.aspx?id=m' + device_id)
    soup_mysite = BeautifulSoup(res.text, "html.parser")
    input_fields = {input.get('name'): input.get('value') for input in soup_mysite.find_all("input")}
    input_fields['ctl00$MainContent$ExportTypeCombo'] = 4
    input_fields['ctl00$MainContent$StartDateTime'] = '01/01/2014',
    input_fields['ctl00$MainContent$EndDateTime'] = '01/01/' + str(date.today().year + 1),
    input_fields['ctl00$MainContent$TimeZoneName'] = 'UTC',
    input_fields['ctl00$MainContent$StartDateUnixTimeStamp'] = 1388534400,
    input_fields['ctl00$MainContent$EndDateUnixTimeStamp'] = int(time.time()) + 10000,

    res = s.post('https://smart-me.com/Details/Export.aspx?id=m' + device_id, data=input_fields)
    if res.status_code != 200 or len(res.text) < 20000:
        raise DownloadException()

    return res.text


